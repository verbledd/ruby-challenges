
weather_today = 'foggy'

case weather_today
when 'sunny'
	puts "Get out and enjoy that beautiful yellow ball in the sky!"
when 'cloudy'
	puts "Sometimes it's cloudy, get over it."
when 'foggy'
	puts "Where are you? I can't see you in this fog!"
when 'rainy'
	puts "Rain rain, go away!"
when 'snowy'
	puts "AHHH, snow!"
end 